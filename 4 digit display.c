#include "main.h"

RTC_HandleTypeDef hrtc;

const uint8_t segmentMap[10] = {
    0b00111111, // 0
    0b00000110, // 1
    0b01011011, // 2
    0b01001111, // 3
    0b01100110, // 4
    0b01101101, // 5
    0b01111101, // 6
    0b00000111, // 7
    0b01111111, // 8
    0b01101111  // 9
};
void display_segments(uint8_t value, int digitPos) {
    uint8_t pattern = segmentMap[value];

    if (digitPos == 1) {
        pattern |= 0b10000000; // Enable DP on Digit 3
    }

    for (int i = 0; i < 8; i++) {
        HAL_GPIO_WritePin(GPIOB, (1 << i), (pattern & (1 << i)) ? GPIO_PIN_SET : GPIO_PIN_RESET);
    }
}

void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_RTC_Init(void);


void enable_digit(int pos) {
    // Turn off all first
    HAL_GPIO_WritePin(GPIOA, GPIO_PIN_0 | GPIO_PIN_1, GPIO_PIN_SET);
    HAL_GPIO_WritePin(GPIOC, GPIO_PIN_0 | GPIO_PIN_1, GPIO_PIN_SET);

    switch (pos) {
        case 0: HAL_GPIO_WritePin(GPIOA, GPIO_PIN_0, GPIO_PIN_RESET); break; // D1
        case 1: HAL_GPIO_WritePin(GPIOA, GPIO_PIN_1, GPIO_PIN_RESET); break; // D2
        case 2: HAL_GPIO_WritePin(GPIOC, GPIO_PIN_0, GPIO_PIN_RESET); break; // D3
        case 3: HAL_GPIO_WritePin(GPIOC, GPIO_PIN_1, GPIO_PIN_RESET); break; // D4
    }
}

int main(void) {
    HAL_Init();
    SystemClock_Config();
    MX_GPIO_Init();
    MX_RTC_Init();

    RTC_TimeTypeDef sTime;
    RTC_DateTypeDef sDate;

    while (1) {
        HAL_RTC_GetTime(&hrtc, &sTime, RTC_FORMAT_BIN);
        HAL_RTC_GetDate(&hrtc, &sDate, RTC_FORMAT_BIN); // Always needed

        int sec = sTime.Seconds;
        uint8_t digits[4] = {0, 0, sec / 10, sec % 10};  // Show MMSS or 00SS

        for (int i = 0; i < 4; i++) {
            display_segments(digits[i], i);  // <-- pass digit position
            enable_digit(i);
            HAL_Delay(5);
        }

    }
}

